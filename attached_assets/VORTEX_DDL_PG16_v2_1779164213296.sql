    created_at          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_pago         PRIMARY KEY (id_pago),
    CONSTRAINT fk_pago_factura FOREIGN KEY (id_factura)
