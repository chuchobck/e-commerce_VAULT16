CREATE TABLE audit_log (
    id_audit        BIGINT          NOT NULL GENERATED ALWAYS AS IDENTITY,
    tabla           VARCHAR(50)     NOT NULL,
    id_registro     VARCHAR(50)     NOT NULL,
    accion          CHAR(6)         NOT NULL,
    payload_antes   JSONB,
    payload_despues JSONB,
    id_usuario_bo   INTEGER,
    id_cliente      INTEGER,
    ip              VARCHAR(45),
    user_agent      VARCHAR(255),
    fecha           TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT pk_audit_log     PRIMARY KEY (id_audit),
    CONSTRAINT fk_aud_usuario   FOREIGN KEY (id_usuario_bo)
        REFERENCES usuarios_backoffice(id_usuario)
        ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT fk_aud_cliente   FOREIGN KEY (id_cliente)
        REFERENCES cliente(id_cliente)
        ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT chk_aud_accion   CHECK (accion IN ('INSERT','UPDATE','DELETE','LOGIN','LOGOUT','EXPORT'))
);

COMMENT ON TABLE audit_log IS
    'Ledger append-only de auditoría general. NUNCA se modifica ni se elimina.';
