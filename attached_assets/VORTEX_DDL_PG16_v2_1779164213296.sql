-- --------------------------------------------------------------------
--  5.3  FACTURA — comprobante de venta (cabecera)
-- --------------------------------------------------------------------
CREATE TABLE factura (
    id_factura          VARCHAR(15)     NOT NULL,
    id_cliente          INTEGER         NOT NULL,
    id_empleado         INTEGER,
    id_direccion_envio  INTEGER,
    subtotal            NUMERIC(14,3)   NOT NULL DEFAULT 0,
    descuento_total     NUMERIC(14,3)   NOT NULL DEFAULT 0,
    total               NUMERIC(14,3)   NOT NULL DEFAULT 0,
    fecha_emision       TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    estado              CHAR(3)         NOT NULL DEFAULT 'EMI',
    created_at          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_factura           PRIMARY KEY (id_factura),
    CONSTRAINT fk_fac_cliente       FOREIGN KEY (id_cliente)
        REFERENCES cliente(id_cliente)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_fac_empleado      FOREIGN KEY (id_empleado)
        REFERENCES empleado(id_empleado)
        ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT fk_fac_direccion     FOREIGN KEY (id_direccion_envio)
        REFERENCES direccion_cliente(id_direccion)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT chk_fac_subtotal     CHECK (subtotal        >= 0),
    CONSTRAINT chk_fac_descuento    CHECK (descuento_total >= 0),
    CONSTRAINT chk_fac_total        CHECK (total           >= 0),
    CONSTRAINT chk_fac_estado       CHECK (estado IN ('EMI','PAG','ENV','ENT','ANU')),
    CONSTRAINT chk_fac_consistencia CHECK (total = subtotal - descuento_total),
    CONSTRAINT chk_fac_formato_sri  CHECK (id_factura ~ '^\d{3}-\d{3}-\d{9}$')
);
COMMENT ON TABLE factura IS
    'Cabecera de factura. Estados: EMI=Emitida, PAG=Pagada, ENV=Enviada, ENT=Entregada, ANU=Anulada';
COMMENT ON COLUMN factura.id_factura IS
    'Formato SRI Ecuador: 001-001-XXXXXXXXX (validado por regex)';
COMMENT ON COLUMN factura.id_empleado IS
    'NULL para ventas online auto-procesadas (sin vendedor humano)';
COMMENT ON COLUMN factura.id_direccion_envio IS
    'Dirección a la que se envía. NULL solo si es retiro en local.';


-- --------------------------------------------------------------------
--  5.4  DETALLE_FACTURA — líneas de venta
-- --------------------------------------------------------------------
CREATE TABLE detalle_factura (
    id_factura      VARCHAR(15)     NOT NULL,
    id_variante     INTEGER         NOT NULL,
    cantidad        INTEGER         NOT NULL,
    precio_unitario NUMERIC(14,3)   NOT NULL,
    descuento_item  NUMERIC(14,3)   NOT NULL DEFAULT 0,
    subtotal_linea  NUMERIC(14,3)   GENERATED ALWAYS AS (
        (cantidad * precio_unitario) - descuento_item
    ) STORED,
    CONSTRAINT pk_detalle_factura   PRIMARY KEY (id_factura, id_variante),
    CONSTRAINT fk_dfa_factura       FOREIGN KEY (id_factura)
        REFERENCES factura(id_factura)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_dfa_variante      FOREIGN KEY (id_variante)
        REFERENCES variante_producto(id_variante)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT chk_dfa_cantidad     CHECK (cantidad        > 0),
    CONSTRAINT chk_dfa_precio       CHECK (precio_unitario >= 0),
    CONSTRAINT chk_dfa_descuento    CHECK (descuento_item  >= 0),
    CONSTRAINT chk_dfa_descuento_max CHECK (descuento_item <= cantidad * precio_unitario)
);
COMMENT ON TABLE detalle_factura IS
    'Líneas de venta. Snapshot de precio_unitario al momento de la compra.';


-- --------------------------------------------------------------------
