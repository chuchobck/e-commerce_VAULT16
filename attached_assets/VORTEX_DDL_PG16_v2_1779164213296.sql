-- --------------------------------------------------------------------
--  2.4  VARIANTE_PRODUCTO — UNIDAD ATÓMICA DEL INVENTARIO  ★ CORE ★
-- --------------------------------------------------------------------
CREATE TABLE variante_producto (
    id_variante         INTEGER         NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    id_producto         VARCHAR(7)      NOT NULL,
    id_talla            INTEGER         NOT NULL,
    color               VARCHAR(30)     NOT NULL,
    sku                 VARCHAR(50)     NOT NULL,

    -- ── Contadores escribibles por la aplicación ─────────────────────
    var_saldo_inicial   INTEGER         NOT NULL DEFAULT 0,
    var_qty_ingresos    INTEGER         NOT NULL DEFAULT 0,
    var_qty_egresos     INTEGER         NOT NULL DEFAULT 0,
    var_qty_ajustes     INTEGER         NOT NULL DEFAULT 0,

    -- ── Saldo calculado: PG es el único dueño ────────────────────────
    var_saldo_final     INTEGER         GENERATED ALWAYS AS (
        var_saldo_inicial
        + var_qty_ingresos
        - var_qty_egresos
        + var_qty_ajustes
    ) STORED,

    created_at          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT pk_variante_producto   PRIMARY KEY (id_variante),
    CONSTRAINT uq_var_sku             UNIQUE      (sku),
    CONSTRAINT uq_var_combinacion     UNIQUE      (id_producto, id_talla, color),

    CONSTRAINT fk_var_producto        FOREIGN KEY (id_producto)
        REFERENCES producto(id_producto)
        ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_var_talla           FOREIGN KEY (id_talla)
        REFERENCES talla(id_talla)
        ON UPDATE CASCADE ON DELETE RESTRICT,

    CONSTRAINT chk_var_saldo_ini      CHECK (var_saldo_inicial  >= 0),
    CONSTRAINT chk_var_qty_ing        CHECK (var_qty_ingresos   >= 0),
    CONSTRAINT chk_var_qty_egr        CHECK (var_qty_egresos    >= 0),

    -- Bloquea cualquier UPDATE que dejaría el stock en negativo
    CONSTRAINT chk_var_stock_no_neg   CHECK (
        (var_saldo_inicial + var_qty_ingresos - var_qty_egresos + var_qty_ajustes) >= 0
    )
);
COMMENT ON TABLE variante_producto IS
    'Unidad atómica de stock: 1 fila = 1 combinación producto+talla+color. Núcleo del inventario.';
COMMENT ON COLUMN variante_producto.var_saldo_final IS
    'GENERATED ALWAYS STORED: ini + ingresos - egresos + ajustes. Solo lectura — escrito por PG.';


