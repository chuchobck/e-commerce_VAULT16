    'INSERT/UPDATE/DELETE = cambios de datos | LOGIN/LOGOUT = sesiones | EXPORT = descarga de datos';
COMMENT ON COLUMN audit_log.payload_antes IS
    'Snapshot del registro ANTES del cambio. NULL en INSERT.';
COMMENT ON COLUMN audit_log.payload_despues IS
    'Snapshot del registro DESPUÉS del cambio. NULL en DELETE lógico solo guarda estado nuevo.';
COMMENT ON COLUMN audit_log.id_usuario_bo IS
    'NULL si la acción la hizo un cliente o un proceso automático del sistema.';
COMMENT ON COLUMN audit_log.id_cliente IS
    'NULL si la acción la hizo un empleado del backoffice.';

CREATE INDEX idx_audit_tabla_registro
    ON audit_log (tabla, id_registro, fecha DESC);
COMMENT ON INDEX idx_audit_tabla_registro IS
    'Acelera "dame el historial completo de este registro específico"';

