    CONSTRAINT chk_pago_metodo CHECK (metodo IN ('STRIPE','TRANSFERENCIA','EFECTIVO','OTRO')),
    CONSTRAINT chk_pago_estado CHECK (estado IN ('PEN','COM','FAL','REE'))
);
COMMENT ON TABLE pago IS
