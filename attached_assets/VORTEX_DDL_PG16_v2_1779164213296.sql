    CONSTRAINT fk_car_cliente       FOREIGN KEY (id_cliente)
        REFERENCES cliente(id_cliente)
        ON UPDATE CASCADE ON DELETE CASCADE
);
COMMENT ON TABLE carrito IS
    'Carrito persistente del cliente (1:1). Se vacía al confirmar una factura.';


-- --------------------------------------------------------------------
--  5.2  CARRITO_DETALLE — líneas apuntando a la VARIANTE
